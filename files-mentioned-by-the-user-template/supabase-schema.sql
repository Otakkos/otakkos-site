on public.site_settings
for insert
to authenticated
with check (true);

create policy "Admins can update site settings"
on public.site_settings
for update
to authenticated
using (true)
with check (true);

create policy "Admins can delete site settings"
on public.site_settings
for delete
to authenticated
using (true);

