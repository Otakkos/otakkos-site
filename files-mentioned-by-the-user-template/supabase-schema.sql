create policy "Public can read uploaded site files"
on storage.objects
for select
to anon, authenticated
using (bucket_id = 'site-uploads');

create policy "Admins can upload site files"
on storage.objects
for insert
to authenticated
with check (bucket_id = 'site-uploads');

create policy "Admins can update site files"
on storage.objects
for update
to authenticated
using (bucket_id = 'site-uploads')
with check (bucket_id = 'site-uploads');

create policy "Admins can delete site files"
on storage.objects
for delete
to authenticated
using (bucket_id = 'site-uploads');
